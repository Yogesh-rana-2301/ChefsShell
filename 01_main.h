#ifndef MAIN_H
#define MAIN_H

#include <stdlib.h>
#include <unistd.h> //for fork and execvp
#include <stdio.h>


/* ANSI Color Codes for Terminal Output Formatting (Short Forms)

** X  - Reset        → "\033[0m"
** R  - Red          → "\033[31m"
** G  - Green        → "\033[32m"
** Y  - Yellow       → "\033[33m"
** B  - Blue         → "\033[34m"
** M  - Magenta      → "\033[35m"
** C  - Cyan         → "\033[36m"
** W  - White        → "\033[37m"
** BD - Bold         → "\033[1m"
** UL - Underline    → "\033[4m"
*/


#define X   "\033[0m"
#define R   "\033[31m"
#define G   "\033[32m"
#define Y   "\033[33m"
#define B   "\033[34m"
#define M   "\033[35m"
#define C   "\033[36m"
#define W   "\033[37m"
#define BD  "\033[1m"
#define UL  "\033[4m"




#endif
